type Options struct {
	Profile     string
	Concurrency int
	DurationS   int
	Schedule    string
	IncludeTags []string
	ExcludeTags []string
	RunSeed     string
	ChaosSeed   string
}

type scenarioYAML struct {
	Version      int              `yaml:"version"`
	Mode         string           `yaml:"mode"`
	Target       targetYAML       `yaml:"target"`
	Workload     workloadYAML     `yaml:"workload"`
	Chaos        chaosYAML        `yaml:"chaos"`
	Expectations expectationsYAML `yaml:"expectations"`
	Seeds        seedsYAML        `yaml:"seeds"`
	Redaction    *redactionYAML   `yaml:"redaction,omitempty"`
}

type targetYAML struct {
	BaseURL string            `yaml:"base_url"`
	Headers map[string]string `yaml:"headers"`
	Auth    authYAML          `yaml:"auth"`
}

type authYAML struct {
	Type           string `yaml:"type"`
	BearerTokenEnv string `yaml:"bearer_token_env,omitempty"`
	UsernameEnv    string `yaml:"username_env,omitempty"`
	PasswordEnv    string `yaml:"password_env,omitempty"`
	APIKeyEnv      string `yaml:"api_key_env,omitempty"`
	In             string `yaml:"in,omitempty"`
	Name           string `yaml:"name,omitempty"`
}

type workloadYAML struct {
	Requests     []requestYAML `yaml:"requests"`
	Concurrency  int           `yaml:"concurrency"`
	DurationS    int           `yaml:"duration_s"`
	ScheduleMode string        `yaml:"schedule_mode"`
	TickMS       int           `yaml:"tick_ms,omitempty"`
}

type requestYAML struct {
	ID       string            `yaml:"id"`
	Method   string            `yaml:"method"`
	Path     string            `yaml:"path"`
	Query    map[string]string `yaml:"query,omitempty"`
	Headers  map[string]string `yaml:"headers,omitempty"`
	Body     *string           `yaml:"body,omitempty"`
	JSONBody any               `yaml:"json_body,omitempty"`
	Weight   int               `yaml:"weight,omitempty"`
}

type seedsYAML struct {
	RunSeed   string `yaml:"run_seed"`
	ChaosSeed string `yaml:"chaos_seed"`
}

type redactionYAML struct {
	Headers []string `yaml:"headers"`
}

var idSanitizer = regexp.MustCompile(`[^A-Za-z0-9_.-]`)

func Generate(source string, opts Options) ([]byte, error) {
	if opts.Concurrency <= 0 {
		opts.Concurrency = 2
	}
	if opts.DurationS <= 0 {
		opts.DurationS = 30
	}
	if opts.Schedule == "" {
		opts.Schedule = "closed_loop"
	}
	if opts.Profile == "" {
		opts.Profile = "light"
	}

	doc, err := LoadSpec(source)
	if err != nil {
		return nil, err
	}

	baseURL := "http://localhost:8080"
	if len(doc.Servers) > 0 && doc.Servers[0].URL != "" {
		baseURL = strings.TrimRight(doc.Servers[0].URL, "/")
	}

	auth, authHeaders := extractAuth(doc)

	requests, err := buildRequests(doc, opts)
	if err != nil {
		return nil, err
	}
	if len(requests) == 0 {
		return nil, fmt.Errorf("no operations found after filtering")
	}

	chaos, expectations, err := ChaosProfile(opts.Profile)
	if err != nil {
		return nil, err
	}

	runSeed := opts.RunSeed
	if runSeed == "" {
		runSeed, err = randomSeed()
		if err != nil {
			return nil, err
		}
	}
	chaosSeed := opts.ChaosSeed
	if chaosSeed == "" {
		chaosSeed, err = randomSeed()
		if err != nil {
			return nil, err
		}
	}

	wl := workloadYAML{
		Requests:     requests,
		Concurrency:  opts.Concurrency,
		DurationS:    opts.DurationS,
		ScheduleMode: opts.Schedule,
	}
	if opts.Schedule == "open_loop" {
		wl.TickMS = 100
	}

	redactHeaders := []string{"authorization", "cookie", "set-cookie", "x-api-key", "date"}
	for _, h := range authHeaders {
		lower := strings.ToLower(h)
		found := false
		for _, r := range redactHeaders {
			if r == lower {
				found = true
				break
			}
		}
		if !found {
			redactHeaders = append(redactHeaders, lower)
		}
	}

	scenario := scenarioYAML{
		Version: 1,
		Mode:    "black",
		Target: targetYAML{
			BaseURL: baseURL,
			Headers: authHeaders,
			Auth:    auth,
		},
		Workload:     wl,
		Chaos:        chaos,
		Expectations: expectations,
		Seeds: seedsYAML{
			RunSeed:   runSeed,
			ChaosSeed: chaosSeed,
		},
		Redaction: &redactionYAML{
			Headers: redactHeaders,
		},
	}

	out, err := yaml.Marshal(&scenario)
	if err != nil {
		return nil, fmt.Errorf("marshal scenario yaml: %w", err)
	}

	header := fmt.Sprintf("# Generated by toollab gen from %s\n", source)
	return append([]byte(header), out...), nil
}

func extractAuth(doc *OpenAPIDoc) (authYAML, map[string]string) {
	headers := map[string]string{}
	if doc.Components == nil || len(doc.Components.SecuritySchemes) == 0 {
		return authYAML{Type: "none"}, headers
	}

	names := make([]string, 0, len(doc.Components.SecuritySchemes))
	for name := range doc.Components.SecuritySchemes {
		names = append(names, name)
	}
	sort.Strings(names)

	scheme := doc.Components.SecuritySchemes[names[0]]

	switch {
	case scheme.Type == "http" && scheme.Scheme == "bearer":
		return authYAML{
			Type:           "bearer",
			BearerTokenEnv: "BEARER_TOKEN",
		}, headers

	case scheme.Type == "http" && scheme.Scheme == "basic":
		return authYAML{
			Type:        "basic",
			UsernameEnv: "BASIC_USER",
			PasswordEnv: "BASIC_PASS",
		}, headers

	case scheme.Type == "apiKey":
		if scheme.In == "header" {
			headers[scheme.Name] = "${" + sanitizeEnvName(scheme.Name) + "}"
			return authYAML{Type: "none"}, headers
		}
		return authYAML{
			Type:      "api_key",
			APIKeyEnv: sanitizeEnvName(scheme.Name),
			In:        scheme.In,
			Name:      scheme.Name,
		}, headers

	default:
		return authYAML{Type: "none"}, headers
	}
}

func buildRequests(doc *OpenAPIDoc, opts Options) ([]requestYAML, error) {
	paths := make([]string, 0, len(doc.Paths))
	for p := range doc.Paths {
		paths = append(paths, p)
	}
	sort.Strings(paths)

	includeSet := toSet(opts.IncludeTags)
	excludeSet := toSet(opts.ExcludeTags)

	var requests []requestYAML
	idCounts := map[string]int{}

	for _, path := range paths {
		pi := doc.Paths[path]
		pathParams := pi.Parameters

		for _, entry := range pi.Operations() {
			op := entry.Op

			if !matchesTags(op.Tags, includeSet, excludeSet) {
				continue
			}

			id := operationID(entry.Method, path, op)
			idCounts[id]++
			if idCounts[id] > 1 {
				id = fmt.Sprintf("%s_%d", id, idCounts[id])
			}

			req := requestYAML{
				ID:     id,
				Method: entry.Method,
				Path:   path,
				Weight: weightForMethod(entry.Method),
			}

			allParams := mergeParams(pathParams, op.Parameters)
			query, hdrs := splitParams(allParams)
			if len(query) > 0 {
				req.Query = query
			}
			if len(hdrs) > 0 {
				req.Headers = hdrs
			}

			if isReadMethod(entry.Method) {
				empty := ""
				req.Body = &empty
			} else {
				body := extractJSONBody(op, doc)
				if body != nil {
					req.JSONBody = body
				} else {
					empty := ""
					req.Body = &empty
				}
			}

			requests = append(requests, req)
		}
	}

	return requests, nil
}

func extractJSONBody(op *Operation, doc *OpenAPIDoc) any {
	if op.RequestBody == nil || op.RequestBody.Content == nil {
		return nil
	}
	mt, ok := op.RequestBody.Content["application/json"]
	if !ok {
		for ct, m := range op.RequestBody.Content {
			if strings.Contains(ct, "json") {
				mt = m
				ok = true
				break
			}
		}
	}
	if !ok || mt.Schema == nil {
		return nil
	}
	body, err := GenerateBody(mt.Schema, doc)
	if err != nil {
		return nil
	}
	if len(body) == 0 {
		return nil
	}
	// Convert to json.RawMessage-compatible format for yaml marshaling
	raw, err := json.Marshal(body)
	if err != nil {
		return body
	}
	var result any
	if err := json.Unmarshal(raw, &result); err != nil {
		return body
	}
	return result
}

func operationID(method, path string, op *Operation) string {
	if op.OperationID != "" {
		return sanitizeID(op.OperationID)
	}
	clean := strings.ReplaceAll(path, "{", "")
	clean = strings.ReplaceAll(clean, "}", "")
	clean = strings.ReplaceAll(clean, "/", "_")
	clean = strings.TrimLeft(clean, "_")
	clean = strings.TrimRight(clean, "_")
	return sanitizeID(strings.ToLower(method) + "_" + clean)
}

func sanitizeID(id string) string {
	return idSanitizer.ReplaceAllString(id, "_")
}

func sanitizeEnvName(name string) string {
	upper := strings.ToUpper(name)
	return strings.ReplaceAll(strings.ReplaceAll(upper, "-", "_"), " ", "_")
}

func isReadMethod(method string) bool {
	switch method {
	case "GET", "HEAD", "OPTIONS", "DELETE":
		return true
	}
	return false
}

func weightForMethod(method string) int {
	if isReadMethod(method) {
		return 2
	}
	return 1
}

func matchesTags(tags []string, include, exclude map[string]struct{}) bool {
	if len(exclude) > 0 {
		for _, t := range tags {
			if _, ok := exclude[t]; ok {
				return false
			}
		}
	}
	if len(include) > 0 {
		for _, t := range tags {
			if _, ok := include[t]; ok {
				return true
			}
		}
		return false
	}
	return true
}

func mergeParams(pathLevel, opLevel []Parameter) []Parameter {
	seen := map[string]struct{}{}
	var out []Parameter
	for _, p := range opLevel {
		key := p.In + ":" + p.Name
		seen[key] = struct{}{}
		out = append(out, p)
	}
	for _, p := range pathLevel {
		key := p.In + ":" + p.Name
		if _, ok := seen[key]; !ok {
			out = append(out, p)
		}
	}
	return out
}

func splitParams(params []Parameter) (query map[string]string, headers map[string]string) {
	for _, p := range params {
		val := "value"
		if p.Example != nil {
			val = fmt.Sprintf("%v", p.Example)
		}
		switch p.In {
		case "query":
			if query == nil {
				query = map[string]string{}
			}
			query[p.Name] = val
		case "header":
			lower := strings.ToLower(p.Name)
			if lower == "authorization" || lower == "accept" || lower == "content-type" {
				continue
			}
			if headers == nil {
				headers = map[string]string{}
			}
			headers[p.Name] = val
		}
	}
	return query, headers
}

func toSet(slice []string) map[string]struct{} {
	if len(slice) == 0 {
		return nil
	}
	m := make(map[string]struct{}, len(slice))
	for _, s := range slice {
		m[s] = struct{}{}
	}
	return m
}

func randomSeed() (string, error) {
	n, err := rand.Int(rand.Reader, big.NewInt(1_000_000))
	if err != nil {
		return "", fmt.Errorf("generate seed: %w", err)
	}
	return n.String(), nil
}
