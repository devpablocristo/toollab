// --- Utilities ---

func extractResourceName(path string) string {
	parts := strings.Split(strings.Trim(path, "/"), "/")
	for i := len(parts) - 1; i >= 0; i-- {
		p := parts[i]
		if strings.HasPrefix(p, "{") || strings.HasPrefix(p, ":") || isVersionSegment(p) || p == "api" {
			continue
		}
		return p
	}
	return ""
}

func hasPathParam(path string) bool {
	return strings.Contains(path, "{") || strings.Contains(path, ":")
}

func singularize(s string) string {
	if strings.HasSuffix(s, "ies") {
		return strings.TrimSuffix(s, "ies") + "y"
	}
	if strings.HasSuffix(s, "ses") || strings.HasSuffix(s, "xes") || strings.HasSuffix(s, "zes") {
		return strings.TrimSuffix(s, "es")
	}
	if strings.HasSuffix(s, "s") && !strings.HasSuffix(s, "ss") {
		return strings.TrimSuffix(s, "s")
	}
	return s
}

func extractPathParams(path string) []d.IntelParam {
	var params []d.IntelParam
	parts := strings.Split(path, "/")
	for _, p := range parts {
		if strings.HasPrefix(p, "{") && strings.HasSuffix(p, "}") {
			name := strings.Trim(p, "{}")
			params = append(params, d.IntelParam{
				Name:       name,
				Type:       guessParamType(name),
				Meaning:    fmt.Sprintf("Identifier for the %s.", name),
				Source:     "ast",
				Confidence: 0.9,
			})
		} else if strings.HasPrefix(p, ":") {
			name := strings.TrimPrefix(p, ":")
			params = append(params, d.IntelParam{
				Name:       name,
				Type:       guessParamType(name),
				Meaning:    fmt.Sprintf("Identifier for the %s.", name),
				Source:     "ast",
				Confidence: 0.9,
			})
		}
	}
	return params
}

func guessParamType(name string) string {
	ln := strings.ToLower(name)
	if strings.Contains(ln, "uuid") {
		return "uuid"
	}
	if ln == "id" || strings.HasSuffix(ln, "_id") || strings.HasSuffix(ln, "id") {
		return "string"
	}
	return "string"
}

func buildOperationID(ep d.EndpointEntry) string {
	if ep.HandlerRef != nil && ep.HandlerRef.Extra.Symbol != "" {
		return ep.HandlerRef.Extra.Symbol
	}
	if ep.HandlerRef != nil && ep.HandlerRef.Label != "" {
		label := ep.HandlerRef.Label
		label = strings.ReplaceAll(label, ".", "_")
		label = strings.ReplaceAll(label, "/", "_")
		return label
	}
	resource := extractResourceName(ep.Path)
	return strings.ToLower(ep.Method) + "_" + resource
}

func astRefsFromEntry(ep d.EndpointEntry) []d.ASTRef {
	var refs []d.ASTRef
	if ep.HandlerRef != nil {
		refs = append(refs, *ep.HandlerRef)
	}
	return refs
}

func httpStatusMeaning(status int) string {
	meanings := map[int]string{
		400: "Bad request: invalid input or missing required fields.",
		401: "Unauthorized: authentication required or invalid credentials.",
		403: "Forbidden: authenticated but insufficient permissions.",
		404: "Not found: resource does not exist.",
		405: "Method not allowed.",
		409: "Conflict: resource already exists or state conflict.",
		422: "Unprocessable entity: validation error.",
		429: "Too many requests: rate limit exceeded.",
		500: "Internal server error.",
		502: "Bad gateway.",
		503: "Service unavailable.",
	}
	if m, ok := meanings[status]; ok {
		return m
	}
	return fmt.Sprintf("HTTP %d response.", status)
}

func maxf(a, b float64) float64 {
	if a > b {
		return a
	}
	return b
}
