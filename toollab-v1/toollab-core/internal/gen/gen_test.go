func TestGeneratePostHasJSONBody(t *testing.T) {
	out, err := Generate("../../../testdata/openapi/petstore.yaml", Options{
		Profile:     "none",
		IncludeTags: []string{"Pets"},
		RunSeed:     "1",
		ChaosSeed:   "1",
	})
	if err != nil {
		t.Fatalf("generate: %v", err)
	}

	var scenario map[string]any
	if err := yaml.Unmarshal(out, &scenario); err != nil {
		t.Fatalf("unmarshal: %v", err)
	}
	wl := scenario["workload"].(map[string]any)
	requests := wl["requests"].([]any)

	found := false
	for _, r := range requests {
		req := r.(map[string]any)
		if req["id"] == "createPet" {
			found = true
			if req["json_body"] == nil {
				t.Error("expected json_body for POST createPet")
			}
			if req["body"] != nil {
				t.Error("POST with json_body should not have body field")
			}
			jb, ok := req["json_body"].(map[string]any)
			if !ok {
				t.Fatalf("expected json_body to be map, got %T", req["json_body"])
			}
			if jb["name"] != "Buddy" {
				t.Errorf("expected name=Buddy from example, got %v", jb["name"])
			}
			if jb["species"] != "dog" {
				t.Errorf("expected species=dog from enum, got %v", jb["species"])
			}
		}
	}
	if !found {
		t.Error("createPet request not found")
	}
}

func TestGenerateRedactsAuthHeaders(t *testing.T) {
	out, err := Generate("../../../testdata/openapi/petstore.yaml", Options{
		Profile:   "none",
		RunSeed:   "1",
		ChaosSeed: "1",
	})
	if err != nil {
		t.Fatalf("generate: %v", err)
	}

	var scenario map[string]any
	if err := yaml.Unmarshal(out, &scenario); err != nil {
		t.Fatalf("unmarshal: %v", err)
	}
	redaction, ok := scenario["redaction"].(map[string]any)
	if !ok {
		t.Fatal("expected redaction section")
	}
	headers, ok := redaction["headers"].([]any)
	if !ok {
		t.Fatal("expected redaction headers array")
	}
	found := false
	for _, h := range headers {
		if h == "authorization" {
			found = true
		}
	}
	if !found {
		t.Error("expected 'authorization' in redaction headers")
	}
}
