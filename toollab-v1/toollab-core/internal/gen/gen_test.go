func TestGenerateFromPetstoreFixture(t *testing.T) {
	out, err := Generate("../../../testdata/openapi/petstore.yaml", Options{
		Profile:     "light",
		Concurrency: 4,
		DurationS:   10,
		Schedule:    "closed_loop",
		RunSeed:     "42",
		ChaosSeed:   "7",
	})
	if err != nil {
		t.Fatalf("generate: %v", err)
	}

	if !strings.Contains(string(out), "# Generated by toollab gen") {
		t.Error("expected header comment")
	}

	var scenario map[string]any
	if err := yaml.Unmarshal(out, &scenario); err != nil {
		t.Fatalf("unmarshal output: %v", err)
	}

	if scenario["version"] != 1 {
		t.Errorf("expected version 1, got %v", scenario["version"])
	}
	if scenario["mode"] != "black" {
		t.Errorf("expected mode black, got %v", scenario["mode"])
	}

	target, ok := scenario["target"].(map[string]any)
	if !ok {
		t.Fatal("expected target map")
	}
	if target["base_url"] != "http://localhost:3000" {
		t.Errorf("expected base_url http://localhost:3000, got %v", target["base_url"])
	}

	auth, ok := target["auth"].(map[string]any)
	if !ok {
		t.Fatal("expected auth map")
	}
	if auth["type"] != "bearer" {
		t.Errorf("expected bearer auth, got %v", auth["type"])
	}

	workload, ok := scenario["workload"].(map[string]any)
	if !ok {
		t.Fatal("expected workload map")
	}
	requests, ok := workload["requests"].([]any)
	if !ok {
		t.Fatal("expected requests array")
	}
	if len(requests) == 0 {
		t.Fatal("expected at least one request")
	}

	seeds, ok := scenario["seeds"].(map[string]any)
	if !ok {
		t.Fatal("expected seeds map")
	}
	if seeds["run_seed"] != "42" {
		t.Errorf("expected run_seed 42, got %v", seeds["run_seed"])
	}
}

